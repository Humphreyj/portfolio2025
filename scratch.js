let justOrdered = []
let thirtyDays = []
let fortyFiveDays = []
let overSixtyDays = []
let received = []

// Current date for comparison
const today = new Date()

// Function to calculate the difference in days
function daysBetween(date1, date2) {
    const oneDay = 24 * 60 * 60 * 1000 // milliseconds in a day
    return Math.round((date2 - date1) / oneDay)
}

// Categorize inspections based on due date
inspections.forEach((inspection) => {
    const dueDate = new Date(inspection.inspection_due) // Keep as Date object
    const requestedDate = new Date(inspection.inspection_requested) // Keep as Date object
    const daysUntilDue = daysBetween(requestedDate, dueDate)

    if (!inspection.inspection_received) {
        if (daysUntilDue < 30) {
            justOrdered.push(inspection)
        } else if (daysUntilDue >= 30 && daysUntilDue < 45) {
            thirtyDays.push(inspection)
        } else if (daysUntilDue >= 45 && daysUntilDue < 60) {
            fortyFiveDays.push(inspection)
        } else if (daysUntilDue >= 60) {
            overSixtyDays.push(inspection)
        }
    } else {
        received.push(inspection)
    }
})

